<?php

// +=
$a = 0;
$a += 10;  // $a = $a + 10

echo $a . "<br>";

// -=
$b = 5;
$b -= 10;  // $b = $b - 10

echo $b . "<br>";

// /=
$c = 45;
$c /= 2;  // $c = $c / 2

echo $c . "<br>";

// *=
$d = 20;
$d *= 2;  // $d = $d * 2

echo $d . "<br>";

// %=
$e = 19;
$e %= 2;  // $e = $e % 2

echo $e . "<br>";

// utilização com variáveis

$x = 10;
$y = 20;

echo $y /= $x . "<br>";
